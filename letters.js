var D2R = Math.PI / 180;

function seg(x1, y1, x2, y2) { return [[x1, y1], [x2, y2]]; }

function arc(cx, cy, rx, ry, a0, a1, n) {
    var pts = [], step = (a1 - a0) / n;
    for (var i = 0; i <= n; i++) {
        var a = (a0 + step * i) * D2R;
        pts.push([cx + rx * Math.cos(a), cy + ry * Math.sin(a)]);
    }
    return pts;
}

function join() {
    var r = [];
    for (var i = 0; i < arguments.length; i++) r = r.concat(arguments[i]);
    return r;
}

